#include <stdio.h>
#include <string.h>
#include <stdlib.h>

// Function to decode the encoded flag
void decode_flag(char *encoded_flag, char *decoded_flag) {
    int i;
    for (i = 0; encoded_flag[i] != '\0'; i++) {
        decoded_flag[i] = encoded_flag[i] ^ 0xAA; // XOR with 0xAA for obfuscation
    }
    decoded_flag[i] = '\0';
}

int main(int argc, char *argv[]) {
    char encoded_flag[] = {
        0xcc, 0xc6, 0xcb, 0xcd, 0xd1, 0xe6, 0x9e, 0xc8, 0x9a, 0xd8, 0x9e, 0xde, 
        0x9a, 0xd8, 0xf5, 0xe9, 0x9a, 0xc7, 0x99, 0xc4, 0xd0, 0x9b, 0xf5, 0xe6, 
        0x9b, 0xc4, 0xdf, 0xd2, 0xd2, 0xf5, 0x98, 0x9a, 0x98, 0x9f, 0x8a, 0x98, 
        0x9b, 0x98, 0x9e, 0x8a, 0xf8, 0xef, 0xd9, 0xfe, 0xcb, 0xc4, 0xfe, 0xcb, 
        0x8a, 0xe4, 0xdf, 0x8a, 0xe6, 0xdf, 0xeb, 0xc7, 0xd7, 0x00
    };
    char static_flag[sizeof(encoded_flag)];
    decode_flag(encoded_flag, static_flag);

    if (argc != 2) {
        printf("Utilizare: %s <flag>\n", argv[0]);
        return 1;
    }

    if (strcmp(argv[1], static_flag) == 0) {
        printf("flag corect\n");
    } else {
        printf("flag incorect, mai incercati\n");
    }

    return 0;
}